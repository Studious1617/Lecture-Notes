package Week2;

public class HelloForLoop {
    public static void main(String[] args) {

        /*For Loops - repeat a particular number of times
        *   Need to specify a loop counter to count the number of repeats
        *   Decide what number the loop counter will start counting at
        *   Also specify what condition the loop will stop under
        *       -Often when the counter gets to a certain value
        *   And, by what amount the counter will change after each iteration
        *   Not necessarily whole numbers, or positive numbers
        *       -You can count backwards and/or use doubles or floats*/

        // for (variable = startingNumber; variable <= endingNumber; variable = variable +/-/ / /*/ numOfSomeSort){System.out.println(variable)}
        for (int x = 0; x < 10; x++){
            System.out.println(x);
        }


    }
}
